macro_rules! impl_extra_traits {
    (($first_type:ident, $first_bits:expr), ($(($second_type:ident, $second_bits:expr)),+ $(,)?)) => {
        $(
            impl_convert!($first_type, $first_bits, $second_type, $second_bits);
            impl_concat_cross_sizes!($first_type, $first_bits, $second_type, $second_bits);
            impl_mul_cross_sizes!($first_type, $first_bits, $second_type, $second_bits);
       )+
    };
}

